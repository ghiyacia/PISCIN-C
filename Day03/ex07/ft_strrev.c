char	*ft_strrev(char *str)
{
	int i;
	int a;
	int c;

	i = 0;
	a = 0;
	while (str[i])
	{
		i++;
	}
	i--;
	while (a < i)
	{
		c = str[a];
		str[a] = str[i];
		str[i] = c;
		a++;
		i--;
	}
	return (str);
}
